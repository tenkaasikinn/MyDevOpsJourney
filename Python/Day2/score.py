nilai = int(input("Masukkan nilai kamu: "))

if nilai >= 80:
    print("Congrats kamu mendapat nilai A, pertahankan!")
elif nilai >= 75:
    print("Keren kamu mendapat nilai B+, gas terus!")
elif nilai >= 70: 
    print("Bagus kamu mendapat nilai B, keep up!")
elif nilai >= 65:
    print("Come on kamu mendapat nilai C+, aku yakin kamu lebih dari ini")
elif nilai >= 60:
    print("Kesempatan pasti datang, kamu mendapat nilai  C")
else:
    print("Akwokwok goblok kuliah yang bener tolol")